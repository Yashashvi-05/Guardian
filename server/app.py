@app.get(
    "/tools",
    summary="Available MCP tool manifest",
    tags=["Environment"],
)
def get_tools() -> Dict[str, Any]:
    """
    Returns all tools available in the current episode, with risk metadata.

    Used by the client's get_tools() method to implement the MCP 'Rug Pull'
    deprovision pattern — the client strips quarantined tools from this list
    before exposing the action space to the Worker agent.

    Critical tools (high-risk) are flagged so the Guardian can apply
    surgical privilege downgrade without fully blocking the Worker.
    """
    all_tools = [
        # Safe tools
        {"name": "read_db",              "risk": "low",      "category": "data_read"},
        {"name": "post_to_forum",        "risk": "medium",   "category": "communication"},
        {"name": "query_data_access_log","risk": "low",      "category": "audit"},
        # Sensitive tools
        {"name": "write_db",             "risk": "high",     "category": "data_write"},
        {"name": "process_payment",      "risk": "high",     "category": "financial"},
        {"name": "modify_firewall",      "risk": "critical", "category": "network"},
        {"name": "modify_iam_policy",   "risk": "critical", "category": "iam"},
        {"name": "register_vendor",     "risk": "high",     "category": "vendor"},
        {"name": "export_data",         "risk": "critical", "category": "data_exfil"},
    ]
    # Check if any tools are currently quarantined in the active session
    quarantined = list(_http_env._gym.env.state.quarantined_tools)  # BUG-10 fix
    available = [t for t in all_tools if t["name"] not in quarantined]
    return {
        "tools": [t["name"] for t in available],
        "tool_details": available,
        "quarantined": quarantined,
        "rug_pull_active": len(quarantined) > 0,
        "episode_id": _http_env.state().episode_id,
    }



# ── HITL: Pending escalations ─────────────────────────────────────────────────

@app.post(
    "/hitl/escalate",
    summary="Create a new HITL escalation (fires n8n → Telegram)",
    tags=["HITL"],
)
def hitl_escalate(body: Dict[str, Any]) -> Dict[str, Any]:
    """
    Create a gray-zone escalation. Fires the n8n webhook which sends a
    Telegram alert with ALLOW / BLOCK / SHADOW inline buttons.

    Body:
        {
          "tool_name": "process_payment",
          "classified_attack": "approval_bypass",
          "risk_score": 0.68,
          "domain": "enterprise",
          "capability_tags": "[STATE_MOD=True|FINANCIAL=High]",
          "guardian_reasoning": "Step 4 calls process_payment...",
          "episode_step": 4
        }
    """
    if not _HITL_AVAILABLE:
        return {"error": "HITL module not available"}

    tool_name = body.get("tool_name", "unknown_tool")
    risk_score = float(body.get("risk_score", 0.65))
    domain = body.get("domain", "enterprise")
    classified_attack = body.get("classified_attack", "")

    # Build counterfactual from domain library
    counterfactual = get_counterfactual(tool_name, domain)

    ctx = hitl_manager.create_escalation(
        tool_name=tool_name,
        tool_arguments=body.get("tool_arguments", {}),
        risk_score=risk_score,
        capability_tags=body.get("capability_tags", ""),
        classified_attack=classified_attack,
        guardian_reasoning=body.get("guardian_reasoning", ""),
        counterfactual=counterfactual,
        domain=domain,
        episode_step=int(body.get("episode_step", 0)),
        episode_id=body.get("episode_id", ""),
    )

    log.info("[HITL] Escalation fired: context_id=%s risk=%.2f", ctx.context_id, risk_score)
    return {
        "created": True,
        "context_id": ctx.context_id,
        "tool_name": tool_name,
        "risk_score": risk_score,
        "domain": domain,
        "counterfactual": counterfactual,
        "whatsapp_message": ctx.to_whatsapp_message(),
        "message": "Escalation created. Telegram alert sent via n8n. Awaiting human decision at /hitl/decision.",
    }


