"""
GUARDIAN Fleet — OpenEnv FastAPI Server
=========================================
Implements the full OpenEnv HTTP + WebSocket protocol:

  POST /reset            → {"observation": ..., "state": ..., "reward": 0, "done": false}
  POST /step             → {"observation": ..., "reward": ..., "done": ..., "info": ..., "state": ...}
  GET  /state            → GuardianState
  WS   /ws               → Per-connection isolated WebSocket session
  GET  /health           → {"status": "ok", ...}
  GET  /web              → Browser UI for manual exploration
  GET  /docs             → OpenAPI docs (auto-generated by FastAPI)

WebSocket message protocol:
  Client → Server:
    {"type": "reset", "options": {...}}
    {"type": "step",  "action": {...}}
    {"type": "state"}

  Server → Client:
    {"type": "reset_result",  "observation": ..., "state": ..., "reward": 0, "done": false}
    {"type": "step_result",   "observation": ..., "reward": ..., "done": ..., "info": ..., "state": ...}
    {"type": "episode_done",  "observation": ..., "state": ..., "message": "..."}
    {"type": "state_result",  "state": ...}
    {"type": "error",         "message": "..."}

Run locally:
    uvicorn server.app:app --host 0.0.0.0 --port 8000 --reload

Or inside Docker (see server/Dockerfile):
    docker build -t guardian-env -f server/Dockerfile .
    docker run -p 8000:8000 guardian-env
"""

