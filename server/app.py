@app.post(
    "/reset",
    summary="Reset episode",
    tags=["Environment"],
)
def reset(body: Optional[Dict[str, Any]] = None) -> Dict[str, Any]:
    """
    Start a new episode.

    Body (optional JSON):
        {"seed": 42}

    Returns the initial observation and episode state.
    """
    global _http_env
    obs = _http_env.reset(options=body)
    state = _http_env.state()
    log.info(f"[REST /reset] episode_id={state.episode_id}")
    return {
        "observation": obs.model_dump(),
        "state": state.model_dump(),
        "reward": 0.0,
        "done": False,
    }


# ── REST: step ────────────────────────────────────────────────────────────────

@app.post(
    "/step",
    summary="Execute one Guardian decision",
    tags=["Environment"],
)
def step(action: GuardianAction) -> Dict[str, Any]:
    """
    Execute one Guardian decision and return the result.

    Body: GuardianAction JSON
        {
          "risk_score": 0.85,
          "intervention": "shadow",
          "attack_type": "prompt_injection",
          "rollback_k": 2,
          "reasoning": "Suspicious write to exfil_log"
        }
    """
    global _http_env
    obs, reward, done, info = _http_env.step(action)
    state = _http_env.state()
    log.info(
        f"[REST /step] step={state.step_count} "
        f"intervention={action.intervention} "
        f"risk={action.risk_score:.2f} "
        f"reward={reward:.4f} done={done}"
    )
    return {
        "observation": obs.model_dump(),
        "reward": reward,
        "done": done,
        "info": info,
        "state": state.model_dump(),
    }


